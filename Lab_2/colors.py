def Black(text):
    return "\033[30m{}".format(text)

def Red(text):
    return "\033[31m{}".format(text)

def Green(text):
    return "\033[32m{}".format(text)

def Yellow(text):
    return "\033[33m{}".format(text)

def Blue(text):
    return "\033[34m{}".format(text)

def Violet(text):
    return "\033[35m{}".format(text)

def Turquoise(text):
    return "\033[36m{}".format(text)

def White(text):
    return "\033[37m{}".format(text)

def Reset(text):
    return "\033[0{}".format(text)